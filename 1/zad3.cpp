#include <iostream>
using namespace std;

int main() {
    // Zmienna typu int
    int liczba = 42;
    
    // Zmienna typu float
    float liczbaZmiennejZmiennoprzecinkowej = 3.14f;
    
    // Zmienna typu char
    char znak = 'A';
    
    // Zmienna typu bool
    bool prawda = true;

    // Wyświetlanie wartości zmiennych
    cout << "Zmienna typu int: " << liczba << endl;
    cout << "Zmienna typu float: " << liczbaZmiennejZmiennoprzecinkowej << endl;
    cout << "Zmienna typu char: " << znak << endl;
    cout << "Zmienna typu bool: " << prawda << endl;

    return 0;
}